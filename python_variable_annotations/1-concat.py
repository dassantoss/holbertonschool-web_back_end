#!/usr/bin/env python3
"""
This module defines a function to concatenate two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: The concatenated string.
    """
    return str1 + str2
